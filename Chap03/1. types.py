x = 7                       # Integer
print('x is {}'.format(x))
print(type(x))

y = 7.0                     # Float
print('y is {}'.format(y))
print(type(y))

z = '7'                     # String
print('z is {}'.format(z))
print(type(z))

a = True                    # Boolean
print('a is {}'.format(a))
print(type(a))

b = None                    # NoneType (not a value)
print('b is {}'.format(b))
print(type(b))